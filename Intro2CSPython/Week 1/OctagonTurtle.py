import turtle

def triangleRight(edge=100):
    turtle.forward(edge)
    turtle.right(120)
    turtle.forward(edge)
    turtle.right(120)
    turtle.forward(edge)
    
def triangleLeft(edge=100):
    turtle.forward(edge)
    turtle.left(120)
    turtle.forward(edge)
    turtle.left(120)
    turtle.forward(edge)    

triangleRight(edge=100)        
triangleLeft(edge=100)  
triangleLeft(edge=100)
triangleLeft(edge=100)
triangleRight(edge=100)
triangleRight(edge=100)




